#Program on Creating and Getting variables of different data types

#Age using int
Age=int(input("Enter Your Age:"))

#Height using float
Height=float(input("Enter your height in cm:"))

#Name using string
Name=input("Enter your name:")

#Student? using boolean
Student=bool(input("Are you a student (True/False):"))

#Output
print("Your Age:",Age)
print("Your Height:",Height)
print("Your Name:",Name)
print("Are you a student?:",Student)